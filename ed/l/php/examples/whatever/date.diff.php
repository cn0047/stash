<?php

$a = array(
    '2012-02-22 02:12:55' => '2012-03-22 02:12:55',
    '2014-06-24 12:32:15' => '2014-06-25 12:35:25',
    '2013-06-24 22:22:25' => '2014-06-25 13:22:05',
    '2013-09-14 12:33:15' => '2014-06-25 12:34:19',
    '2013-01-01 12:35:13' => '2014-12-31 22:32:27',
    '2011-02-31 02:32:15' => '2014-12-31 12:42:55',
    '2011-02-30 12:12:15' => '2011-02-31 01:22:21',
    '2011-02-30 22:32:14' => '2011-02-30 12:12:05',
    '2011-08-10 14:10:15' => '2012-09-28 17:31:15',
    '2011-02-27 12:12:25' => '2012-02-28 12:33:09',
    '2013-06-24 12:22:25' => '2014-06-25 12:32:15',
    '2013-09-14 12:33:15' => '2014-06-25 12:32:15',
    '2013-01-01 12:35:13' => '2014-12-31 12:32:15',
    '2013-01-01 12:35:13' => '0000-00-00 00:00:00',
);
foreach ($a as $d1 => $d2) {
    echo "$d1   -   $d2   -   ";
    $diff1 = intval((strtotime($d2)-strtotime($d1))/60/60/24);
    $d1 = new DateTime($d1);
    $d2 = new DateTime($d2);
    $diff = $d1->diff($d2);
    $diff2 = $diff->format('%a');
    if ($diff1 == $diff2) {
        echo "ok\n";
    } else {
        echo "error | $diff1  <> $diff2 \n";
    }
}
echo "\n";
